# El operador multiplicación o repeteición (*) permite multiplicar números y repetir strings

# Multiplicación numérica

5 * 3       # 15
1.5 * 2     # 3.0

# Repetición de strings

"Hola" * 3  # "HolaHolaHola"    (<--- El numero por el cual se esta repitiendo el string tiene que ser entero)

# Operador compuesto

x = 2
x *= 3      # 6                 (<--- x = x * 3)