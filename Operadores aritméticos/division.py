# El operador division (/) permite dividir números

# División numérica (OJO, la división de números siempre va a dar un decimal (float) como resultado, aunque sea exacta)

5 / 2     # 2.5
4 / 2     # 2.0

6 / 0     # Error

# Operador compuesto}

x = 10
x /= 2    # 5.0             (x = x / 2)

# División entera (//)

# La división entera trunca el resultado y devuelve la parte entera

5 / 2     # 2.5
5 // 2    # 2