# El operador suma (+) permite sumar números o concatenar texto

# Suma numérica

5 + 6       # 11
10.3 + .1   # 10.4

x = 4
y = 10

x + y       # 14

# Operador compuesto

z = 10
z += 8      # 18            (z = z + 8)

