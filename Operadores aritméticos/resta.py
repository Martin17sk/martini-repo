# El operador resta (-) permite restar números o cambiar el signo de un número

# Resta numérica

5 - 2       # 3
3.5 - 1.1   # 2.4

# Cambio de signo

x = 5
-x          # -5

# ---------------------------------

y = -6
-y          # 6

# Operador compuesto

z = 5
z -= 2      # 3             (z = z - 2)